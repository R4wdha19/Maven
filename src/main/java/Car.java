public class Car {
	private String[] signs;
	private String side;

	public String[] getSigns() {
		return signs;
	}

	public void setSigns(String[] signs) {
		this.signs = signs;
	}

	// Getter Methods

	public String getSide() {
		return side;
	}

	// Setter Methods

	public void setSide(String side) {
		this.side = side;
	}
}
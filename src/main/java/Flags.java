public class Flags {
	private String png;
	private String svg;

	// Getter Methods

	public String getPng() {
		return png;
	}

	public String getSvg() {
		return svg;
	}

	// Setter Methods

	public void setPng(String png) {
		this.png = png;
	}

	public void setSvg(String svg) {
		this.svg = svg;
	}
}